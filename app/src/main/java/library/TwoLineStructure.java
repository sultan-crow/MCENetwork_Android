package library;

public class TwoLineStructure {

    public String title;
    public String subtext;
    public String subtext_right;

    public TwoLineStructure() {
        super();
    }

    public TwoLineStructure(String title, String subtext, String subtext_right) {

        this.title = title;
        this.subtext = subtext;
        this.subtext_right = subtext_right;

    }
}
